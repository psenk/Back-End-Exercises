public class CarKey {

    private String VIN;
    private boolean laserCut;

    public String getVIN() {
        return VIN;
    }

    public void setVIN(String vIN) {
        VIN = vIN;
    }

    public boolean isLaserCut() {
        return laserCut;
    }
    
    public void setLaserCut(boolean laserCut) {
        this.laserCut = laserCut;
    }

}